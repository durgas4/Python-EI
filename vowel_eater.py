# Prompt the user to enter a word\
user_word=""
word=str(input("enter a word"))
# and assign it to the user_word variable.
for i in word:
    # Complete the body of the for loop.
    if i=="a":

        continue
    elif i=="e":#conditional execution
        continue
    elif i=="o":
        continue
    elif i=="u":
        continue
    else:
        user_word+=i
        print(user_word)
